cards = (["Ada", "Brendan", "Ali"], "birthday");
function writeCards(cards) {
  for (let i = 0; i < cards.length; i++) {
    console.log(`Thank you, ${cards[[i]]}, for the wonderful ${cards[length - 1]} gift`)
  }
  return cards;
}
writeCards(cards);

